def create_markdown_document(categorized_models: Dict[str, List[Dict[str, Any]]], output_file: str):
    """Create a user-friendly markdown document"""
    
    with open(output_file, 'w', encoding='utf-8') as f:
        f.write("# Tess AI Models and Generators\n\n")
        f.write("A comprehensive list of AI models and generators available on Tess AI platform.\n\n")
        
        # Table of contents
        f.write("## Table of Contents\n\n")
        for category, models in categorized_models.items():
            if models:
                f.write(f"- [{category}](#{category.lower().replace(' ', '-')})\n")
        f.write("\n")
        
        # Summary statistics
        total_models = sum(len(models) for models in categorized_models.values())
        f.write(f"## Summary\n\n")
        f.write(f"**Total Models:** {total_models}\n\n")
        
        for category, models in categorized_models.items():
            if models:
                f.write(f"- **{category}:** {len(models)} models\n")
        f.write("\n")
        
        # Detailed sections
        for category, models in categorized_models.items():
            if not models:
                continue
                
            f.write(f"## {category}\n\n")
            
            for model in models:
                title = translate_portuguese_terms(model['title'])
                description = translate_portuguese_terms(model['description'])
                
                f.write(f"### {title}\n\n")
                
                if description:
                    f.write(f"**Description:** {description}\n\n")
                
                if model['url']:
                    f.write(f"**Direct Link:** [{title}]({model['url']})\n\n")
                
                if model['category']:
                    f.write(f"**Category:** {model['category']}\n\n")
                
                if model['stats']:
                    f.write("**Stats:**\n")
                    for stat, value in model['stats'].items():
                        f.write(f"- {stat.title()}: {value}\n")
                    f.write("\n")
                
                if model['creator']:
                    f.write(f"**Creator:** {model['creator']}\n\n")
                
                f.write("---\n\n")

def remove_duplicates(models: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """Remove duplicate models based on title and URL"""
    seen = set()
    unique_models = []
    
    for model in models:
        # Create a unique key based on title and URL
        title = model.get('title', '').strip().lower()
        url = model.get('url', '').strip()
        
        # Create unique identifier
        unique_key = f"{title}|{url}"
        
        if unique_key not in seen:
            seen.add(unique_key)
            unique_models.append(model)
        else:
            print(f"Duplicate found and removed: {model.get('title', 'Unknown')}")
    
    return unique_models

