def extract_model_info(html_file_path: str) -> List[Dict[str, Any]]:
    """Extract all AI model information from the HTML file"""
    
    print(f"Reading HTML file: {html_file_path}")
    
    with open(html_file_path, 'r', encoding='utf-8') as file:
        content = file.read()
    
    soup = BeautifulSoup(content, 'html.parser')
    
    # Find all model cards using the correct selector
    model_cards = soup.find_all('div', class_='card-1')
    
    if not model_cards:
        # Try alternative selectors
        model_cards = soup.find_all('div', attrs={'data-filter': True})
    
    if not model_cards:
        # Try finding cards by other patterns
        model_cards = soup.find_all('div', class_=re.compile(r'card'))
    
    print(f"Found {len(model_cards)} potential model cards")
    
    models = []
    
    for i, card in enumerate(model_cards):
        try:
            # Extract model title
            title_elem = card.find('h4')
            if not title_elem:
                title_elem = card.find('h3') or card.find('h2') or card.find('h1')
            
            if not title_elem:
                continue
                
            title = title_elem.get_text(strip=True)
            
            # Extract description
            desc_elem = card.find('div', class_='card-description')
            if not desc_elem:
                desc_elem = card.find('p')
            
            description = desc_elem.get_text(strip=True) if desc_elem else ""
            
            # Extract URL - look for the correct generator URL
            url_elem = card.find('a', href=lambda href: href and '/dashboard/user/ai/generator/' in href)
            if not url_elem:
                # Fallback to any link
                url_elem = card.find('a', href=True)
            
            url = url_elem['href'] if url_elem else ""
            
            # Make sure URL is absolute
            if url and not url.startswith('http'):
                url = 'https://tess.pareto.io' + url
            
            # Extract thumbnail/image - look for avatar or card images
            img_elem = card.find('img', src=True)
            image_url = ""
            if img_elem:
                image_url = img_elem['src']
                # Handle relative paths to the local files directory
                if image_url and image_url.startswith('./Tess AI _ List of generators_files/'):
                    # Convert to absolute local path
                    image_url = '/home/user1/shawndev1/' + image_url[2:]
                elif image_url and not image_url.startswith('http'):
                    image_url = 'https://tess.pareto.io' + image_url
            
            # Extract category/filter
            category = card.get('data-filter', '')
            
            # Extract stats (likes, runs, etc.)
            stats = {}
            
            # Look for like counts
            like_elem = card.find('span', class_='like-count')
            if like_elem:
                stats['likes'] = like_elem.get_text(strip=True)
            
            # Look for run counts
            run_elem = card.find('span', class_='run-count')
            if run_elem:
                stats['runs'] = run_elem.get_text(strip=True)
            
            # Extract creator info
            creator_elem = card.find('img', class_='avatar')
            creator = ""
            if creator_elem:
                creator = creator_elem.get('alt', '')
            
            if title:  # Only add if we have a title
                models.append({
                    'title': title,
                    'description': description,
                    'url': url,
                    'image_url': image_url,
                    'category': category,
                    'stats': stats,
                    'creator': creator
                })
                
        except Exception as e:
            print(f"Error processing card {i}: {e}")
            continue
    
    print(f"Successfully extracted {len(models)} models")
    return models

